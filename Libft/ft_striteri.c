#include "libft.h"

/*Applies the function f to each character in the string, passing the index
of the character to be modified as the function pointer's first argument*/

void	ft_striteri(char *s, void (*f)(unsigned int, char *))
{
	unsigned int	i;

	i = 0;
	if (!s || !f)
		return ;
	while (s[i])
	{
		f(i, &s[i]);
		i++;
	}
}
